//Día de la semana en entero
let day =2;

if(day==1){
    console.log(`El día de la semana número ${day} es el Lunes`);
}else if(day==2){
    console.log(`El día de la semana número ${day} es el Martes`);
}else if(day==3){
    console.log(`El día de la semana número ${day} es el Miercoles`);
}else if(day==4){
    console.log(`El día de la semana número ${day} es el Jueves`);
}else if(day==5){
    console.log(`El día de la semana número ${day} es el Viernes`);
}else if(day==6){
    console.log(`El día de la semana número ${day} es el Sábado`);
}else if(day==7){
    console.log(`El día de la semana número ${day} es el Domingo`);
}else{
    console.log("Día invalido!");
}

